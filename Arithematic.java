package basicprogram;

public class Arithematic {
	static void add()
	{
		int a=5;
		int b=5;
		int c=a+b;
		System.out.println(c);
	}
	static void sub()
	{
		int a=15;
		int b=5;
		int c=a-b;
		System.out.println(c);
	}
	static void mul()
	{
		int a=5;
		int b=5;
		int c=a*b;
		System.out.println(c);
	}
	static void div()
	{
		int a=15;
		int b=5;
		int c=a/b;
		System.out.println(c);
	}
	static void mod()
	{
		int a=15;
		int b=4;
		int c=a%b;
		System.out.println(c);
	}


public static void main(String[] args)
{
		add();
		sub();
		mul();
		div();
		mod();
		System.out.println("Hello World");
		
		
}
}
	
	


 